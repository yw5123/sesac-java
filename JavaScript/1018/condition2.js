// boolean
if (true) {
  console.log(true);
}

// 비교 연산자
if (1 < 2) {
  console.log(true);
}

// 논리 연산자
if (true && true) {
  console.log(true);
}

// 숫자
if (1) {
  console.log(1);
}

if (-1) {
  console.log(-1);
}

if (0) {
  console.log('실행 안됨');
}

// 문자열
if ('abc') {
  console.log('abc');
}

if ('') {
  console.log('빈 문자열');
}

if (' ') {
  console.log('공백');
}

if (null) {
  console.log('null');
}
